  private void limparDadosTela(){
    jTextFieldIdContato.setText("");
    jTextFieldNome.setText("");
    jTextFieldEmail.setText("");
    jFormattedTextFieldTelefone.setValue(null);
    idEmEdicao = null;
  }
  private boolean emailValido(String email) {
    if (email == null) return false;
    email = email.trim();
    if (email.isEmpty()) return false;

    int posArroba = email.indexOf('@');
    int posPonto  = email.lastIndexOf('.');

    if (posArroba <= 0) return false;
    if (posPonto <= posArroba + 1) return false;
    if (posPonto == email.length() - 1) return false;

    return true;
  }
  private void jButtonIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIncluirActionPerformed
    // TODO add your handling code here:
     try {
        int idContato = Integer.parseInt(jTextFieldIdContato.getText().trim());
        String nome = jTextFieldNome.getText().trim();
        String email = jTextFieldEmail.getText().trim();
        String telefoneTexto = jFormattedTextFieldTelefone.getText();

        if (nome.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Informe o nome.");
            return;
        }
        if (email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Informe o e-mail.");
            return;
        }
        if (!emailValido(email)) {
            JOptionPane.showMessageDialog(this, "E-mail inválido.");
            return;
        }

        Telefone telefone = Telefone.fromFormattedString(telefoneTexto);
        if (telefone == null) {
            JOptionPane.showMessageDialog(this, "Informe o telefone.");
            return;
        }

        Contato objetoContato = new Contato(idContato, nome, email, telefone);
        objContatoCTL.incluir(objetoContato);

        limparDadosTela();
        mostrarListaDeContatos();
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Identificador deve ser um número inteiro.");
    } catch (Exception erro) {
        JOptionPane.showMessageDialog(this, erro.getMessage());
    }
  }//GEN-LAST:event_jButtonIncluirActionPerformed

  private void jButtonConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConsultarActionPerformed
    // TODO add your handling code here:
    try {
        Contato c = null;

        String txtId = jTextFieldIdContato.getText().trim();
        String nome = jTextFieldNome.getText().trim();

        if (!txtId.isEmpty()) {
            int id = Integer.parseInt(txtId);
            c = objContatoCTL.buscarID(id);
        } else if (!nome.isEmpty()) {
            c = objContatoCTL.consultar(nome);
        } else {
            JOptionPane.showMessageDialog(this,
                    "Informe o identificador OU o nome para consultar.");
            return;
        }

        if (c == null) {
            JOptionPane.showMessageDialog(this, "Contato não encontrado.");
            return;
        }

        idEmEdicao = c.getIdContato();

        jTextFieldIdContato.setText(String.valueOf(c.getIdContato()));
        jTextFieldNome.setText(c.getNomeCompleto());
        jTextFieldEmail.setText(c.getEmail());
        if (c.getTelefone() != null) {
            jFormattedTextFieldTelefone.setText(c.getTelefone().toString());
        } else {
            jFormattedTextFieldTelefone.setValue(null);
        }

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Identificador deve ser um número inteiro.");
    } catch (Exception erro) {
        JOptionPane.showMessageDialog(this, erro.getMessage());
    }
    
  }//GEN-LAST:event_jButtonConsultarActionPerformed

    private void jButtonAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarActionPerformed
        // TODO add your handling code here:
        try {
            if (idEmEdicao == null) {
                JOptionPane.showMessageDialog(this,
                        "Consulte um contato antes de alterar.");
                return;
            }

            String txtIdCampo = jTextFieldIdContato.getText().trim();
            if (!txtIdCampo.equals(String.valueOf(idEmEdicao))) {
                JOptionPane.showMessageDialog(this,
                        "Não é permitido alterar o identificador.\n" +
                        "O ID será restaurado.");
                jTextFieldIdContato.setText(String.valueOf(idEmEdicao));
                return;
            }

            String nome = jTextFieldNome.getText().trim();
            String email = jTextFieldEmail.getText().trim();
            String telefoneTexto = jFormattedTextFieldTelefone.getText();

            if (nome.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Informe o nome.");
                return;
            }
            if (email.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Informe o e-mail.");
                return;
            }
            if (!emailValido(email)) {
                JOptionPane.showMessageDialog(this, "E-mail inválido.");
                return;
            }

            Telefone telefone = Telefone.fromFormattedString(telefoneTexto);
            if (telefone == null) {
                JOptionPane.showMessageDialog(this, "Informe o telefone.");
                return;
            }

            Contato objetoContato = new Contato(idEmEdicao, nome, email, telefone);
            objContatoCTL.alterar(objetoContato);

            limparDadosTela();
            mostrarListaDeContatos();
            JOptionPane.showMessageDialog(this, "Contato alterado com sucesso.");
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(this, erro.getMessage());
        }
    }//GEN-LAST:event_jButtonAlterarActionPerformed

    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        // TODO add your handling code here:
        try {
            String txtId = jTextFieldIdContato.getText().trim();
            if (txtId.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Informe o identificador para excluir.");
                return;
            }

            int id = Integer.parseInt(txtId);

            int opcao = JOptionPane.showConfirmDialog(this,
                    "Deseja realmente excluir o contato " + id + "?",
                    "Confirmação", JOptionPane.YES_NO_OPTION);
            if (opcao != JOptionPane.YES_OPTION) {
                return;
            }

            objContatoCTL.excluir(id);

            limparDadosTela();
            mostrarListaDeContatos();
            JOptionPane.showMessageDialog(this, "Contato excluído com sucesso.");
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Identificador deve ser um número inteiro.");
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(this, erro.getMessage());
        }
    }//GEN-LAST:event_jButtonExcluirActionPerformed

