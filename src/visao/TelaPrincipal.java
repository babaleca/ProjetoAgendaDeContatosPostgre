    private void mostrarListaDeContatos() throws Exception{
      try {
        ArrayList<Contato> listaDeContatos = objContatoCTL.listagemDeContatos();
        DefaultTableModel model =  (DefaultTableModel) jTableDadosDosContatos.getModel();
        model.setNumRows(0);
        if(listaDeContatos.isEmpty()) return;
        for (Contato pessoa : listaDeContatos) {
            String[] linha = new String[4];
            linha[0] = pessoa.getIdContato() + "";
            linha[1] = pessoa.getNomeCompleto();
            linha[2] = pessoa.getEmail();

            String telefoneStr = "";
            if (pessoa.getTelefone() != null) {
                telefoneStr = pessoa.getTelefone().toString();
            }
            linha[3] = telefoneStr;

            model.addRow(linha);
        }
      } catch (Exception erro) {
        throw erro;
      }
    }

